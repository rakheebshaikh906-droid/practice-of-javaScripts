
//de-structuring
let person = {
    name: "John",
    age: 30,
    city: "New York"
};
//de-structuring assignment
let { name, age, city } = person;
console.log(name); // "John"
console.log(age); // 30
console.log(city); // "New York"
//de-structuring with arrays
let arr = [1, 2, 3, 4, 5];
let [a, b, c] = arr;
